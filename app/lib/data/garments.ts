export const getGarmentsByCategory = () => {
  return Object.values(GARMENT_CATEGORIES).map(category => ({
    category,
    items: GARMENT_DATA.filter(item => item.category === category)
  }));
};

export const getGarmentById = (id: string) => {
  return GARMENT_DATA.find(item => item.id === id);
}; 